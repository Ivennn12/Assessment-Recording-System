    private void btnChangePasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChangePasswordActionPerformed
 
        // Show an input dialog to get the current password from the admin
        String currentPassword = JOptionPane.showInputDialog(this, "Enter current password:");

        // Check if the admin entered a valid current password (not null or empty)
        if (currentPassword != null && !currentPassword.isEmpty()) {
            // Get the current admin username from somewhere (e.g., a variable called 'currentAdmin')
            String currentAdmin = "Admin"; 

            // Validate the current password for the current admin
            if (AdminHandler.validateAdminPassword(currentAdmin, currentPassword)) {
                // Show another input dialog to get the new password from the admin
                String newPassword = JOptionPane.showInputDialog(this, "Enter new password:");

                // Check if the admin entered a valid new password (not null or empty)
                if (newPassword != null && !newPassword.isEmpty()) {
                    // Call the method to update the password in the database
                    AdminHandler.updateAdminPassword(newPassword);
                    JOptionPane.showMessageDialog(this, "Password updated successfully!");
                } else {
                    // If the admin entered a null or empty new password, show a message
                    JOptionPane.showMessageDialog(this, "Invalid new password. Please try again.");
                }
            } else {
                // If the current password is incorrect, show a message
                JOptionPane.showMessageDialog(this, "Incorrect current password. Please try again.");
            }
        } else if (currentPassword == null) {
            // If the admin clicked the 'Cancel' button in the current password input dialog
            JOptionPane.showMessageDialog(this, "Password change canceled.");
        } else {
            // If the admin entered a null or empty current password, show a message
            JOptionPane.showMessageDialog(this, "Invalid current password. Please try again.");
        }
        updateHintAndPasswordFields();
    }//GEN-LAST:event_btnChangePasswordActionPerformed


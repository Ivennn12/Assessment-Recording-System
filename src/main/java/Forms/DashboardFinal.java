    private void txtMNameKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtMNameKeyTyped
        
        char c = evt.getKeyChar();
        String middleName = txtMName.getText();

        // Check if the middle name has already reached two letters
        if (middleName.length() >= 2 && !(c == KeyEvent.VK_DELETE || c == KeyEvent.VK_BACK_SPACE)) {
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(null, "Middle name should have a maximum of two letters.");
            return; // Stop processing the key event further if the limit is reached
        }
        
        // Check if the input is a letter
        if (!(Character.isAlphabetic(c) || c == KeyEvent.VK_DELETE || c == KeyEvent.VK_BACK_SPACE)) {
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(null, "Accepts only letter as input.");
        }                 
    }//GEN-LAST:event_txtMNameKeyTyped


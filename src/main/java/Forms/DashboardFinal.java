    private void cbxAssessmentTypeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbxAssessmentTypeActionPerformed
        
        // Set condition fo cbxAssessmentType
        String selectedCategory = (String) cbxAssessmentType.getSelectedItem();
        if (selectedCategory.equals("SELECT")) {
            spnrAssessmentNum.setEnabled(false);
            spnrAssessmentNum.setValue(1);
            txtNumOfItems.setEditable(false);
            txtTotalScore.setEditable(false);
        }
        else if (selectedCategory.equals("Quiz") || selectedCategory.equals("Activity") || selectedCategory.equals("Assignment")) {
            spnrAssessmentNum.setEnabled(true);
            JFormattedTextField spin = ((JSpinner.DefaultEditor)spnrAssessmentNum.getEditor()).getTextField();
            spin.setEditable(false);
            txtNumOfItems.setEditable(true);
            txtTotalScore.setEditable(true);
        }
        else if (selectedCategory.equals("Midterm") || selectedCategory.equals("Final")) {
            spnrAssessmentNum.setEnabled(false);
            spnrAssessmentNum.setValue(1);
            txtNumOfItems.setEditable(true);
            txtTotalScore.setEditable(true);
        }
    }//GEN-LAST:event_cbxAssessmentTypeActionPerformed


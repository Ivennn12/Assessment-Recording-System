    private void cbxSubjectsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbxSubjectsActionPerformed
        // TODO add your handling code here:

        String selectedSubject = (String) cbxSubjects.getSelectedItem();

        // Check if a subject is selected
        if (selectedSubject != null) {
            int subjectId = 0; // Initialize subjectId variable

            // Retrieve the subject ID based on the selected subject
            try (Connection conn = DatabaseConnector.getConnection()) {
                String query = "SELECT subject_id FROM subjects WHERE subject_name = ?";
                PreparedStatement pstmt = conn.prepareStatement(query);
                pstmt.setString(1, selectedSubject);
                ResultSet rs = pstmt.executeQuery();

                // Check if a matching subject was found
                if (rs.next()) {
                    // Retrieve the subject ID from the result set
                    subjectId = rs.getInt("subject_id");
                }

                conn.close(); // Close the database connection
            } catch (SQLException ex) {
                ex.printStackTrace();
            }

            // Retrieve the subject details from the database based on the selected subject_id
            try (Connection conn = DatabaseConnector.getConnection()) {
                String query = "SELECT subject_code, description FROM subjects WHERE subject_id = ?";
                PreparedStatement pstmt = conn.prepareStatement(query);
                pstmt.setInt(1, subjectId);
                ResultSet rs = pstmt.executeQuery();

                // Check if a matching subject was found
                if (rs.next()) {
                    // Retrieve the subject code and description from the result set
                    String subjectCode = rs.getString("subject_code");
                    String description = rs.getString("description");

                    // Populate the text fields with the retrieved data
                    txtScode.setText(subjectCode);
                    txtDescription.setText(description);
                }

                conn.close(); // Close the database connection
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_cbxSubjectsActionPerformed


    private void updateHintAndPasswordFields() {
        
        String administrator = "Admin";
        // Get the latest hint and password values from the database
        String password = AdminHandler.getPasswordForAdmin(administrator);
        String hint = AdminHandler.getHintForAdmin(administrator);

        // Update the hint and password fields if they have changed
        if (!txtHint.getText().equals(hint)) {
            txtHint.setText(hint);
        }
        if (!txtPassword.getText().equals(password)) {
            txtPassword.setText(password);
        }
    }
    
